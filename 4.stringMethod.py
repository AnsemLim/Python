name = "ansem lim "
# calculate the words
print(len(name))

# calculate the same word
print(name.count("m"))

# find the word (the code start with 0)
print(name.find("s"))

# capitalize (but only the first words)
print(name.capitalize())

# uppercase 
print(name.upper())

# lowercase 
print(name.lower())

# digit (if digit is true)
print(name.isdigit())

# alpha (if no space include is true,else false)
print(name.isalpha())

# replace character(first is what should be replace,second is what should be replace with)
print(name.replace("a","i"))

# extra 
print(name*3)

